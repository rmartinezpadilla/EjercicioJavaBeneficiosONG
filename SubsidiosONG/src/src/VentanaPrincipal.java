    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        // TODO add your handling code here:
        if (txtnombre.getText().length()==0 || txtapellidos.getText().length()==0 || txtpromedio.getText().length()==0 || txtvalorsemestre.getText().length()==0) {
            JOptionPane.showMessageDialog(null, "Por favor ingresa los datos faltantes.", "Importante",  JOptionPane.ERROR_MESSAGE);
        }else{
            Double temp;
        
        nombres=txtnombre.getText();
        apellidos=txtapellidos.getText();
        promedio=Double.parseDouble(this.txtpromedio.getText());
        valormatricula=Double.parseDouble(this.txtvalorsemestre.getText());
        if (promedio>3 && promedio<=3.5){
                temp=(valormatricula*20)/100;
                dineroVeinte= dineroVeinte+temp;
                subsidioveinte++;
                cantidadSubsidiados++;
            }else if (promedio>=3.6 && promedio<=3.9){
                temp=(valormatricula*40)/100;
                dineroCuarenta= dineroCuarenta+temp;
                subsidiocuarenta++;
                cantidadSubsidiados++;
            }else if (promedio>=4.0 && promedio<=4.5){
                temp=(valormatricula*60)/100;
                dineroSesenta= dineroSesenta+temp;
                subsidiosesenta++;
                cantidadSubsidiados++;
            }else if (promedio>=4.6 && promedio<=5.0){
                temp=(valormatricula*80)/100;
                dineroOchenta= dineroOchenta+temp;
                subsidioochenta++;
                cantidadSubsidiados++;
            }else{
                cantidadNoSubsidiados++;
            }
        
         objectName[tempposicion]=new Estudiante(nombres, apellidos, promedio, valormatricula);
               
           txtnombre.setText("");
           txtapellidos.setText("");
           txtpromedio.setText("");
           txtvalorsemestre.setText("");

           if (cantidad_estudiantes>=3) {
           btncalcular.setEnabled(true); 
           btnguardar.setEnabled(false);
           
           txtnombre.setEnabled(false);
           txtapellidos.setEnabled(false);
           txtpromedio.setEnabled(false);
           txtvalorsemestre.setEnabled(false);
          
           }
           
           cantidad_estudiantes++;   
           tempposicion++; 
        }
        
        
    }//GEN-LAST:event_btnguardarActionPerformed

    private void btncalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncalcularActionPerformed
        // TODO add your handling code here:
        for (int i = 0; i<objectName.length; i++){
            objectName[i].darSubsidio();
        }
        
        lblnumafiliados.setText(Integer.toString(cantidadSubsidiados));
        lblsubsidioveinte.setText(Integer.toString(subsidioveinte));
        lblsubsidiocuarenta.setText(Integer.toString(subsidiocuarenta));
        lblsubsidiosesenta.setText(Integer.toString(subsidiosesenta));
        lblsubsidioochenta.setText(Integer.toString(subsidioochenta));
        lblsubsidiona.setText(Integer.toString(tamarray-cantidadSubsidiados));
        lbldineroveinte.setText(Double.toString(dineroVeinte));
        lbldinerocuarenta.setText(Double.toString(dineroCuarenta));
        lbldinerosesenta.setText(Double.toString(dineroSesenta));
        lbldineroochenta.setText(Double.toString(dineroOchenta));
        lblporcentajeveinte.setText(Double.toString((subsidioveinte*100)/tamarray) + " %");
        lblporcentajecuarenta.setText(Double.toString((subsidiocuarenta*100)/tamarray) + " %");
        lblporcentajesesenta.setText(Double.toString((subsidiosesenta*100)/tamarray) + " %");
        lblporcentajeochenta.setText(Double.toString((subsidioochenta*100)/tamarray) + " %");
        lblporcentajena.setText(Double.toString(((tamarray-cantidadSubsidiados)*100)/tamarray) + " %");
        btncomenzar.setEnabled(true);
        btncalcular.setEnabled(false);
        btnmostrardatos.setEnabled(true);
        
        
         for (int i = 0; i<objectName.length; i++){           
           // System.out.println(objectName[i].dameDatos());
           lbltodo.setText((objectName[i].dameDatos()));
        }
         
        
        
        
    }//GEN-LAST:event_btncalcularActionPerformed

